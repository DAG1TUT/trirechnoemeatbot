def detect_category(description: str) -> str:
    """Определяет категорию по описанию траты."""
    text = description.lower().strip()
    for category, keywords in CATEGORIES.items():
        if category == "💳 Прочее":
            continue
        for kw in keywords:
            if kw in text:
                return category
    return "💳 Прочее"


def get_all_categories():
    """Список всех категорий (без Прочее для справки)."""
    return [c for c in CATEGORIES if c != "💳 Прочее"]
